def get_advice(disease_class: str) -> str:
    """Возвращает рекомендации по лечению в зависимости от диагноза."""
    disease_lower = disease_class.lower()

    if "healthy" in disease_lower:
        return "✅ Растение здорово! Продолжайте стандартный уход."

    if any(word in disease_lower for word in ["scab", "rust", "mildew", "blight", "spot", "mold", "fungal"]):
        return ("🍄 *Грибковое заболевание*\n\n"
                "1. Удалите пораженные листья и плоды.\n"
                "2. Обработайте фунгицидом (Скор, Хорус, Квадрис).\n"
                "3. Обеспечьте проветривание и снизьте влажность.\n"
                "4. Повторите обработку через 7–10 дней.")

    if "bacterial" in disease_lower:
        return ("🧪 *Бактериальное заболевание*\n\n"
                "1. Удалите больные части растения.\n"
                "2. Обработайте медьсодержащими препаратами.\n"
                "3. Продезинфицируйте инструменты.\n"
                "4. Поливайте под корень, не попадая на листья.")

    if any(word in disease_lower for word in ["virus", "mosaic", "curl"]):
        return ("🦠 *Вирусное заболевание*\n\n"
                "⚠️ Вирусные болезни не лечатся!\n"
                "1. Изолируйте или уничтожьте растение.\n"
                "2. Боритесь с насекомыми-переносчиками (тля, белокрылка).")

    if any(word in disease_lower for word in ["mite", "spider", "insect"]):
        return ("🐛 *Вредители*\n\n"
                "1. Смойте вредителей водой или мыльным раствором.\n"
                "2. Обработайте инсектицидом (Актара, Конфидор).\n"
                "3. При небольшом поражении соберите вручную.")

    return ("🌱 *Рекомендация*\n\n"
            "Внимательно осмотрите растение. При признаках заболевания "
            "обработайте универсальным фунгицидом и обеспечьте оптимальные условия.")
