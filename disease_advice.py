from disease_translator import get_crop_name


def get_advice(disease_class: str) -> str:
    """
        Возвращает рекомендации по лечению на русском языке
        """
    disease_lower = disease_class.lower()

    # Здоровые растения
    if "healthy" in disease_lower:
        crop = get_crop_name(disease_class)
        # Убираем лишние символы из названия культуры
        clean_crop = crop.replace('_', ' ').replace('(', '').replace(')', '')
        return (f"✅ *Растение здорово!*\n\n"
                f"Рекомендации по уходу за {clean_crop}:\n"
                f"• Продолжайте регулярный полив\n"
                f"• Обеспечьте достаточное освещение\n"
                f"• Проводите профилактические осмотры\n"
                f"• Подкармливайте по сезону")

    # Грибковые заболевания
    if any(word in disease_lower for word in ["scab", "rust", "mildew", "blight", "spot", "mold", "fungal"]):
        return ("🍄 *Грибковое заболевание*\n\n"
                "*Немедленные действия:*\n"
                "1. Удалите все пораженные листья и плоды\n"
                "2. Сожгите растительные остатки\n\n"
                "*Обработка:*\n"
                "• Опрыскайте фунгицидом (Скор, Хорус, Квадрис)\n"
                "• Обработайте медьсодержащим препаратом\n"
                "• Повторите через 7-10 дней\n\n"
                "*Профилактика:*\n"
                "• Обеспечьте проветривание\n"
                "• Не переувлажняйте почву\n"
                "• Мульчируйте прикорневую зону")

    # Бактериальные заболевания
    if "bacterial" in disease_lower:
        return ("🧪 *Бактериальное заболевание*\n\n"
                "*Немедленные действия:*\n"
                "1. Изолируйте больное растение\n"
                "2. Удалите все пораженные части\n"
                "3. Продезинфицируйте инструменты\n\n"
                "*Обработка:*\n"
                "• Обработайте препаратами с медью\n"
                "• Используйте Фитоспорин-М\n"
                "• Опрыскайте раствором марганцовки\n\n"
                "*Важно:*\n"
                "• Поливайте только под корень\n"
                "• Не допускайте попадания воды на листья")

    # Вирусные заболевания
    if any(word in disease_lower for word in ["virus", "viral", "mosaic", "curl"]):
        return ("🦠 *Вирусное заболевание*\n\n"
                "*ВНИМАНИЕ:* Вирусные болезни не лечатся!\n\n"
                "*Что делать:*\n"
                "1. Немедленно изолируйте растение\n"
                "2. Удалите все пораженные части\n"
                "3. При сильном поражении уничтожьте растение\n"
                "4. Продезинфицируйте все инструменты\n\n"
                "*Профилактика:*\n"
                "• Боритесь с насекомыми-переносчиками (тля, белокрылка)\n"
                "• Покупайте здоровый посадочный материал\n"
                "• Соблюдайте севооборот")

    # Вредители
    if any(word in disease_lower for word in ["mite", "spider", "insect", "pest"]):
        return ("🐛 *Вредители*\n\n"
                "*Механические методы:*\n"
                "• Смойте вредителей струей воды\n"
                "• Соберите насекомых вручную\n"
                "• Установите ловушки\n\n"
                "*Народные средства:*\n"
                "• Опрыскайте мыльным раствором\n"
                "• Используйте настой чеснока или табака\n"
                "• Обработайте золой\n\n"
                "*Химическая обработка:*\n"
                "• Примените инсектицид (Актара, Конфидор)\n"
                "• Обработайте акарицидом от клещей")

    # Универсальный совет
    return ("🌱 *Рекомендации:*\n\n"
            "1. Внимательно осмотрите растение\n"
            "2. Удалите подозрительные листья\n"
            "3. Обеспечьте оптимальные условия\n"
            "4. При необходимости обработайте универсальным фунгицидом\n\n"
            "📞 Для точной диагностики обратитесь к агроному")